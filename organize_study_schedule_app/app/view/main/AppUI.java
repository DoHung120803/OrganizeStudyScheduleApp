        private int menuBarWidth = 283;
        private int menuBarHeight = 611;

        // phương thức mở menu khi click vào close btn
        private void openMenuBar() {
                // tạo luồng chạy song song với luồng chính Main
                new Thread(new Runnable() {
                        @Override
                        public void run() {
                                for (int i = 0; i < menuBarWidth; i++) {
                                        menuBar.setSize(i, menuBarHeight);
                                        try {
                                                Thread.sleep(1);
                                        } catch (InterruptedException ex) {
                                                Logger.getLogger(AppUI.class.getName()).log(Level.SEVERE, null, ex);
                                        }
                                }
                        }
                }).start();
        }

        // phương thức đóng menu khi click vào close btn
        private void closeMenuBar() {
                // tạo luồng chạy song song với luồng chính Main
                new Thread(new Runnable() {
                        @Override
                        public void run() {
                                for (int i = menuBarWidth; i >= 0; i--) {
                                        menuBar.setSize(i, menuBarHeight);
                                        try {
                                                Thread.sleep(1);
                                        } catch (InterruptedException ex) {
                                                Logger.getLogger(AppUI.class.getName()).log(Level.SEVERE, null, ex);
                                        }
                                }
                        }
                }).start();
        }

        private void centerFrame() {
                java.awt.Dimension screenSize = java.awt.Toolkit.getDefaultToolkit().getScreenSize();
                int screenWidth = screenSize.width;
                int screenHeight = screenSize.height;

                int frameWidth = getWidth();
                int frameHeight = getHeight();

                int x = (screenWidth - frameWidth) / 2;
                int y = (screenHeight - frameHeight) / 2;

                setLocation(x, y);
        }

